<h1>Features: Customer Details</h1>

<h3>Telephone Number Lookup</h3>

<p>Retrieve customer details by entering only the 
telephone number. Chopstix will display previously 
entered name, address, and special instructions from 
previous orders.</p>

<h3>Area Code Pre-Entry</h3>

<p>Select a local area code and Chopstix will pre-enter 
the area code into the telephone number lookup. Reduce 
typing and speed order entry.</p>

<h3>Telephone Number Extension</h3>

<p>Enter an internal extension number for corporate 
customers to bypass the operator or for late night orders.</p>

<h3>Apartment and Building Entry</h3>

<p>The unit number in a multi-tenant building is entered 
separately from the building address allowing it to be 
clearly displayed on the customer receipt for delivery 
orders. Chopstix can also store additional building 
entry instructions to assist drivers in successfully 
delivering the order such as building entry codes, 
instructions to outdoor events and room numbers for 
corporate orders.</p>

<h3>Main Intersection</h3>

<p>Enter driving directions for difficult to find 
addresses, major landmarks, or main cross streets to 
assist drivers in locating the address.</p>

<h3>Repeat Order Count</h3>

<p>Chopstix displays the number of times the customer has 
ordered. Use the count to identify regular customers or 
for new customer specials.</p>

<h3>Multiple Delivery Methods</h3>

<p>Select at order time the method of delivery which will 
be printed on the receipt enabling you to separate 
orders by delivery method. Keep pickup orders hot by 
the door while orders for delivery can be made ready 
for fast driver turnaround.</p>

<h3>Customer Last Order Recall</h3>

<p>Retrieve all the items from the customer's last order 
and load them into the current order. Quickly repeat a 
favourite order, or helpfully suggest items the 
customer previously enjoyed.<p>

<h3>Change Customer Telephone Number</h3>

<p>Transfer all customer details to a new telephone 
number, no retyping required and all order history is 
preserved with the new number.</p>

<h3>Complaints and Account Credits</h3>

<p>When a customer has a complaint about an order, 
preserve the customer relationship and ensure a future 
order by recording the complaint and applying a credit 
to the next purchase or purchases. Chopstix 
automatically applies the credit balance to future 
orders until the balance is exhausted.</p>

<h3>Special Instructions</h3>

<p>When a customer has a special request, store it with 
the customer details. Surprise your customers by 
remembering their previous requests during the order 
process. All special instructions will be printed on 
both kitchen and customer receipts.</p>

<h1>Features: Order Entry</h1>

<h3>Menu Code Item Entry</h3>

<p>Enter items based on your own menu codes to keep pace 
with customer order speed. Simply enter quantity and 
code to complete the item and the full name and price 
are automatically inserted.</p>

<h3>Real Time Totals</h3>

<p>All totals including taxes, discounts, delivery charges 
and specials are displayed on-screen as each item is entered.</p>

<h3>Per Item Instructions</h3>

<p>Insert special instructions to any individual item on 
the order. Swap ingredients, specify cooking 
instructions or reinforce allergy requirements. All 
item instructions are printed on both kitchen and 
customer receipts.</p>

<h3>Item Style Choices</h3>

<p>Each menu item may have multiple styles assigned to 
differentiate similar items. Some uses include choice 
of sauce type, cooking length or main ingredient.</p>

<h3>Item Extras</h3>

<p>Each menu item may have multiple unvalued items 
attached to it for inclusion on the printed receipt. 
Use extra items to include specific condiments, 
cutlery, or prepackaged additional items such as soup 
crackers or dry noodles.</p>

<h3>Combo Items</h3>

<p>Chopstix allows the creation of special combo menu 
items which contain one or more sub-items with a 
package price. Each sub-item is individually displayed 
on the order and all normal item features such as 
styles, extras, and special instructions are available.</p>

<h3>Combo Item Override and Adjustment</h3>

<p>Change any combo sub-item, allowing substitution of 
combo components. The price of the combo can be 
manually adjusted up or down on each sub-item to 
account for the price difference between the original 
item and its replacement.</p>

<h3>Multiple Payment Methods</h3>

<p>Select a payment type during the order process and it 
will be printed clearly on the receipt to assist with 
external payment processing. Use the payment method to 
ease reconciling daily totals for cash, credit card and 
debit purchases.</p>

<h3>Credit Card Information</h3>

<p>Gather customer credit card information during the 
order process to ensure cardholder signature by 
printing card info and signature line on the receipt.</p>

<h3>Void Payments</h3>

<p>The void payment method allows the creation of 
specially designated orders that can be used to account 
for non-standard events. Chopstix balances the need for 
accountability with the reality of canceled orders, 
refused deliveries, and special circumstances.</p>

<h3>Custom Business Rules</h3>

<p>Chopstix supports running custom business rules to 
implement policies such as waived delivery charges, 
free items, specials, cash and pickup discounts, linked 
items, and more. Contact us for more information.</p>

<h3>Cash Helper</h3>

<p>When a Cash payment is chosen the Cash Helper window 
will appear during receipt printing to assist with 
calculation of change to be returned based on cash tendered.</p>

<h3>Receipt Reprint</h3>

<p>Chopstix will reprint an order receipt when necessary, 
such as during printer failure or the original receipt 
is damaged during order preparation.</p>

<h3>Discount</h3>

<p>A manual discount in dollars or percent can be applied 
to any order. Reward loyal customers, set favourable 
pricing for large orders, or handle off-menu items with 
negative discounts.</p>

<h3>Delivery Charge</h3>

<p>A manual delivery charge can be set or waived that 
overrides normal business rules charges.</p>

<h3>Quick New Order</h3>

<p>Immediately abort the current order and begin a new 
order with a single keystroke.</p>

<h3>Recall Last</h3>

<p>Retrieve the last posted order without first entering 
the customer telephone number.</p>

<h3>Multiple Local Terminals</h3>

<p>Chopstix supports multiple additional terminals 
networked to the main terminal. No additional licence required.</p>

<h1>Features: Receipt Printing</h1>

<h3>Two Column Receipt</h3>

<p>Chopstix generates a split receipt format that places 
the kitchen and customer copy of the receipt side by 
side for use with perforated paper. Both receipts are 
printed simultaneously to avoid lost or mixed-up 
receipts in a busy establishment.</p>

<h3>Order Time</h3>

<p>The exact time that the order was posted is printed on 
both receipts. Useful for determining wait and delivery times.</p>

<h3>Delivery Method</h3>

<p>The method of delivery is included in the details 
portion of the receipt to prevent incorrect delivery.</p>

<h3>Payment Method</h3>

<p>The method of payment is clearly printed at the bottom 
of the receipt to prevent orders requiring additional 
actions such as physical card swipe from being 
delivered before payment.</p>

 <h3>Per Item Instructions</h3>

<p>Each item special instruction is clearly indicated on 
both receipts to ensure the instruction is complied 
with by staff.</p>

<h3>Tax Registration</h3>

<p>In jurisdictions that require a tax registration number 
declaration on the receipt, one or both tax 
registration numbers may be printed on the receipt.</p>

<h3>Credit Card Signature Line</h3>

<p>A signature line is printed on the receipt when a 
credit card is used to order. Avoid charge-backs by 
obtaining the customer signature at delivery.</p>

<h3>Custom Business Rule Items</h3>

<p>Items added as a result of custom business rules are 
printed in the order portion of the receipt below a 
blank space to clearly differentiate. This allows 
behaviour such as sauce choices or free items at 
certain purchase levels to be appended to the order.</p>

<h3>Item Extras</h3>

<p>Menu item extras are printed below the order section of 
the receipt to clearly indicate the difference from 
normal items. If these items are prepackaged, this 
section can be used to speed order packing during preparation.</p>

<h3>Credit Remaining</h3>

<p>When an account credit has been applied to an order, or 
a manual negative balance was placed on the account, 
the credit balance remaining is printed on the receipt.</p>

<h3>Simple Receipt</h3>

<p>Optimized for line printers, the simple design of the 
receipt permits high speed printing to keep pace with orders.</p>

<h3>Receipt Quality and Speed Settings</h3>

<p>Increase the quality or the speed of receipt printing 
based on printer capabilities and business requirements.</p>

<h3>Receipt Print Queue</h3>

<p>Once an order has been posted, the print job for the 
receipt is queued and Chopstix is ready to take a new 
order while the receipt is still printing. When 
multiple terminals are in use, a single receipt printer 
may be shared.</p>

<h1>Features: Administration</h1>

<h3>Web-based Menu Manager</h3>

<p>Use a standard web browser to maintain the menu for 
Chopstix. Add new items, change prices, redefine 
specials all through standard web forms.</p>

<h3>Menu Item Generations</h3>

<p>Chopstix tracks changes to the menu so it can preserve 
order history while allowing menu changes at any time. 
Retrieving an old order will always show the menu item 
as it was at the time of the order.</p>

<h3>Daily Report</h3>

<p>Show all orders for the day including daily totals for 
subtotal, discount, delivery, taxes and total receipts. 
Orders may be individually opened to view item details, 
and any special instructions posted with the order.</p>

<h3>Opensource Database Format</h3>

<p>All information gathered by Chopstix is stored in an 
opensource database format provided by SQLite. Create 
additional reports by querying the database directly, 
and rest assured your customer and order data is not 
locked into a proprietary format.</p>

<h3>Backups</h3>

<p>The Chopstix database can be backed up in an industry 
standard SQL statement format. Use the backup to 
restore Chopstix or export it to another database for 
your own use.</p>

